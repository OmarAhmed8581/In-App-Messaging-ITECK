package com.itecknologi.iteckapp;

public class PastSeven {
    double Friday;
    double Monday;
    double Saturday;
    double Sunday;
    double Thursday;
    double Tuesday;
    double Wednesday;

    public PastSeven(double friday, double monday, double saturday, double sunday, double thursday, double tuesday, double wednesday) {
        Friday = friday;
        Monday = monday;
        Saturday = saturday;
        Sunday = sunday;
        Thursday = thursday;
        Tuesday = tuesday;
        Wednesday = wednesday;
    }

    public double getFriday() {
        return Friday;
    }

    public void setFriday(double friday) {
        Friday = friday;
    }

    public double getMonday() {
        return Monday;
    }

    public void setMonday(double monday) {
        Monday = monday;
    }

    public double getSaturday() {
        return Saturday;
    }

    public void setSaturday(double saturday) {
        Saturday = saturday;
    }

    public double getSunday() {
        return Sunday;
    }

    public void setSunday(double sunday) {
        Sunday = sunday;
    }

    public double getThursday() {
        return Thursday;
    }

    public void setThursday(double thursday) {
        Thursday = thursday;
    }

    public double getTuesday() {
        return Tuesday;
    }

    public void setTuesday(double tuesday) {
        Tuesday = tuesday;
    }

    public double getWednesday() {
        return Wednesday;
    }

    public void setWednesday(double wednesday) {
        Wednesday = wednesday;
    }
}
